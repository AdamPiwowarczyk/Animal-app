export class Animal {
    id: number;
    name: string;
    weight: number;
    legs: number;
    img: string;
    
    constructor(id: number, name: string, weight: number, legs: number, img: string){
        this.id = id;
        this.name = name;
        this.weight = weight;
        this.legs = legs;
        this.img = img;
    }
    
}
